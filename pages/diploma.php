                            <p>The program combines theoretical knowledge with hands-on clinical training, enabling students to work confidently in hospitals, clinics, and community health settings. Students are trained under experienced faculty and gain real-world exposure through hospital postings and practical sessions.</p>
                            <h4>What You Will Learn</h4>
                            <p>Throughout the General Nursing 3-Year Diploma Course, students gain strong theoretical knowledge along with extensive practical training to become competent and compassionate nursing professionals. The course covers the fundamentals of nursing and nursing ethics, along with anatomy and physiology to build a solid understanding of the human body. Students are trained in medical-surgical nursing, learning essential patient care techniques, infection control measures, hospital safety practices, and emergency care including first aid and nutrition management.</p>
                            <p>In addition, the program emphasizes holistic and community-based care through community health nursing and public health awareness. Students also receive specialized training in midwifery and maternity care, child health and pediatric nursing, and mental health nursing with patient counseling. Alongside clinical skills, the course focuses on developing effective communication skills, professionalism, and ethical behavior, preparing students to work confidently in diverse healthcare environments.</p>
