                            <h3>Course Overview</h3>
                            <p>The Post Basic B.Sc. Nursing program at Florence College of Nursing is a 2-year undergraduate degree course designed for registered nurses who wish to advance their professional knowledge, clinical competence, and leadership skills. This program builds upon prior nursing education and focuses on developing a deeper understanding of advanced nursing concepts, evidence-based practice, and modern healthcare delivery systems.</p>
                         
                            <p>The course integrates theoretical learning with intensive clinical exposure, enabling students to enhance their expertise in patient care, nursing management, and research-oriented practice. With guidance from experienced faculty and hands-on training in well-equipped healthcare settings, students are prepared to meet the evolving demands of the nursing profession. Emphasis is placed on critical thinking, ethical practice, and professional development to prepare graduates for higher responsibilities in hospitals, educational institutions, and community health organizations.</p>
                            <h4>What You Will Learn</h4>
                            <p>Throughout the Post Basic B.Sc. Nursing 2-Year Course, students gain advanced knowledge in nursing sciences, clinical practice, and healthcare management. The curriculum includes advanced medical-surgical nursing, community health nursing, mental health nursing, and maternal and child health nursing, enabling students to deliver comprehensive and specialized patient care. Strong emphasis is given to nursing education, research methodology, and evidence-based practice to enhance analytical and academic skills.</p>
                            <p>In addition, students are trained in nursing administration and leadership, healthcare ethics, communication skills, and professional behavior. The program also focuses on quality assurance, patient safety, and modern healthcare technologies. By the end of the course, graduates are well prepared for leadership roles, higher studies, and diverse career opportunities in clinical practice, teaching, administration, and public health sectors.</p>
